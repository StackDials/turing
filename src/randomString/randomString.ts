function randomString(size: number, caseInsensitive = false): string {
  let string = "";
  const limit = caseInsensitive ? 36 : 62;

  for (let i = 0; i < size; ++i) {
    string += ALPHANUMERIC[randomRangeInt(0, limit)];
  }

  return string;
}

